#!/bin/bash

## -----------------------------------------------------------------------
## Intent: Debug function, display repo branches
## -----------------------------------------------------------------------
function show_branch()
{
    local __ver="$1"; shift

    cat <<EOS

** -----------------------------------------------------------------------
** ${FUNCNAME[0]}
** -----------------------------------------------------------------------
EOS
    git branch -a 2>&1 | grep "$__ver"
    return
}

## -----------------------------------------------------------------------
## -----------------------------------------------------------------------
function is_branch()
{
    local arg="$1"; shift

    readarray -t branches < <(git branch -a)
    [[ " ${branches[@]} " =~ " $arg " ]] && { true; } || { false; }
    return
}

: # $?=0

